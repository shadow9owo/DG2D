#pragma once

namespace DG2D
{
    typedef struct Vector2I
    {
        int x;
        int y;
    } Vector2I;

    typedef struct Vector2
    {
        float x;
        float y;
    } Vector2;

    typedef struct Vector3I
    {
        int x;
        int y;
        int z;
    } Vector3I;

    typedef struct Vector3
    {
        float x;
        float y;
        float z;
    } Vector3;

    typedef struct Vector4
    {
        float x;
        float y;
        float z;
        float w;
    } Vector4;
}